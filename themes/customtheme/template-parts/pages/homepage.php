      $total = $wpdb->get_var( "SELECT COUNT(*) FROM $table_name" );

      // Tentukan jumlah data per halaman
      $per_page = 5;

      // Hitung total halaman
      $total_pages = ceil( $total / $per_page );

      // Tentukan halaman saat ini
      $current_page = max( 1, get_query_var( 'paged' ) );

      // Hitung offset
      $offset = ( $current_page - 1 ) * $per_page;

      // Query dengan batasan (limit) dan offset
      $results = $wpdb->get_results( "SELECT * FROM $table_name LIMIT $per_page OFFSET $offset" );

      if ( $results ) {
          foreach ( $results as $result ) {
              echo "<p>ID: {$result->id}, Nama: {$result->firstname}</p>"; // Ganti 'id' dan 'nama' dengan kolom yang ada di tabel Anda
          }
      } else {
          echo 'Tidak ada data yang ditemukan.';
